package pojo;

import domain.Lottle;

public class LottleInfo {
	private String userName;
	private Lottle lottle;
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public Lottle getLottle() {
		return lottle;
	}
	public void setLottle(Lottle lottle) {
		this.lottle = lottle;
	}
}
